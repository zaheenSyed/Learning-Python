# Integers and Floating point numbers
# int & float

print(2 + 4)
print(2 - 4)
print(2 * 4)
print(2 / 4)
print("\n")

print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))
print(type(0))
print("\n")

print(type(20 + 1.1))
print(type(9.9 + 1.1))
print(9.9 + 1.1)
print("\n")

print((2 ** 3))     # to the power of
print(5 // 3)       # integer division rounded down
print(5 % 3)        # modulo / remainder
print("\n")

# Maths functions
# Round
print(round(3.1))
print(round(3.9))

# Absolute values
print(abs(-20))
print("\n")

# Complex Numbers
complex()

# Binary numbers
print(bin(5))
print(int("0b101", 2))      # ("number", base of number)
print("\n")
